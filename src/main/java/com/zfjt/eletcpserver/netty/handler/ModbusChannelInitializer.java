package com.zfjt.eletcpserver.netty.handler;

import io.netty.channel.ChannelInitializer;
import io.netty.channel.ChannelPipeline;
import io.netty.channel.socket.SocketChannel;
import io.netty.handler.codec.bytes.ByteArrayEncoder;
import io.netty.handler.codec.string.StringDecoder;
import io.netty.handler.codec.string.StringEncoder;
import io.netty.handler.timeout.IdleStateHandler;
import io.netty.handler.timeout.ReadTimeoutHandler;
import io.netty.handler.timeout.WriteTimeoutHandler;
import lombok.RequiredArgsConstructor;
import org.springframework.stereotype.Component;

import java.util.concurrent.TimeUnit;

/**
 * Channel Initializer
 *
 * @author Jibeom Jung akka. Manty
 */
@Component
@RequiredArgsConstructor
public class ModbusChannelInitializer extends ChannelInitializer<SocketChannel> {

    private final MessageHandler messageHandler;
    private final StringEncoder stringEncoder = new StringEncoder();
    private final StringDecoder stringDecoder = new StringDecoder();

    @Override
    protected void initChannel(SocketChannel socketChannel) {
        ChannelPipeline pipeline = socketChannel.pipeline();
        pipeline.addLast(new ByteArrayEncoder());
        pipeline.addLast(new ReadTimeoutHandler(2, TimeUnit.MINUTES));
        pipeline.addLast(messageHandler);
    }
}
